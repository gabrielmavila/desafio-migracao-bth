INSERT INTO CIDADES
SELECT
	CID.CODIGO,
	EST.id_estados,
	CID.NOME,
	CID.SIAFI	
FROM TEMP_CIDADE AS CID
LEFT JOIN ESTADOS EST
ON ( EST.UF = CID.UF);
